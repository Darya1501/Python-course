# Преобразования введенной строки

string = input()

# количество символов:
print(len(string))

# последний символ:
print(string[-1])

# первые два символа:
print(string[:2])

# последние два символа:
print(string[:-2])

# строкa в обратном порядке:
print(string[::-1])

# строка без первого и последнего символа:
print(string[1:-1])